import math
si = math.sin(5)
co = math.cos(10)
tn = math.tan(15)
print ("แปลงค่ามุม sine(5) =", math.degrees(si), "องศา")
print ("แปลงค่ามุม cos(10) =", math.degrees(co), "องศา")
print ("แปลงค่ามุม tan(15) =", math.degrees(tn), "องศา")
print ("แปลงค่ามุม 90 องศา =", math.radians(90), "เรเดียน")